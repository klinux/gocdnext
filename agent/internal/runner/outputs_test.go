func TestParseOutputsFile_MissingDeclaredKey(t *testing.T) {
	work := t.TempDir()
	path, _ := prepareOutputsFile(work, "job-B")
	// Plugin wrote NEXT but operator declared `kind: KIND` — the
	// missing one fails the job.
	_ = os.WriteFile(path, []byte("NEXT='v1.0.0'\n"), 0o600)
	_, err := parseOutputsFile(path, map[string]string{
		"next": "NEXT",
		"kind": "KIND",
	})
	if err == nil {
		t.Fatal("expected error for missing declared key")
	}
	if !strings.Contains(err.Error(), "kind") {
		t.Errorf("error should cite the missing alias, got: %v", err)
	}
}

