func TestPrepareOutputsFile_CreatesEmpty0600File(t *testing.T) {
	work := t.TempDir()
	path, err := prepareOutputsFile(work, "abcdef1234567890-1111-2222-3333-444455556666")
	if err != nil {
		t.Fatalf("prepareOutputsFile: %v", err)
	}
	info, err := os.Stat(path)
	if err != nil {
		t.Fatalf("stat: %v", err)
	}
	if info.Size() != 0 {
		t.Errorf("size = %d, want 0 (file should be empty before the plugin writes)", info.Size())
	}
	// On unix, file mode must be 0o600 — verify the bits aren't 077-leaky.
	if info.Mode().Perm() != 0o600 {
		t.Errorf("mode = %v, want 0600", info.Mode().Perm())
	}
	// Parent dir should be 0o700 and live under the agent-chosen
	// .gocdnext/outputs subtree.
	dirInfo, err := os.Stat(filepath.Dir(path))
	if err != nil {
		t.Fatalf("stat dir: %v", err)
	}
	if dirInfo.Mode().Perm() != 0o700 {
		t.Errorf("dir mode = %v, want 0700", dirInfo.Mode().Perm())
	}
	if !strings.Contains(path, filepath.Join(".gocdnext", "outputs")) {
		t.Errorf("path %q not under .gocdnext/outputs", path)
	}
}

func TestPrepareOutputsFile_TruncatesExisting(t *testing.T) {
	// Reused workspace (k8s shared-mode reuse, agent retries) — a
	// leftover file from a prior run must not contaminate this run.
	work := t.TempDir()
	jobID := "0000-1111-2222-3333"
	path, err := prepareOutputsFile(work, jobID)
	if err != nil {
		t.Fatalf("first prepare: %v", err)
	}
	if err := os.WriteFile(path, []byte("STALE=value\n"), 0o600); err != nil {
		t.Fatalf("write stale: %v", err)
	}
	if _, err := prepareOutputsFile(work, jobID); err != nil {
		t.Fatalf("second prepare: %v", err)
	}
	info, _ := os.Stat(path)
	if info.Size() != 0 {
		t.Errorf("prepareOutputsFile didn't truncate (size = %d)", info.Size())
	}
}

func TestParseOutputsFile_HappyPath_FiltersToDeclared(t *testing.T) {
	work := t.TempDir()
	path, err := prepareOutputsFile(work, "job-A")
	if err != nil {
		t.Fatal(err)
	}
	// Plugin writes MORE than the operator declared — extra keys
	// are silently dropped, the declared subset is rekeyed to
	// alias → value.
	content := `# Generated by gocdnext/semver-bump
NEXT='v1.3.0'
CURRENT='v1.2.5'
KIND='minor'
PREV_SHA='abc123'
`
	if err := os.WriteFile(path, []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}
	declared := map[string]string{
		"next": "NEXT",
		"kind": "KIND",
	}
	out, err := parseOutputsFile(path, declared)
	if err != nil {
		t.Fatalf("parse: %v", err)
	}
	if len(out) != 2 {
		t.Fatalf("len = %d, want 2 (%v)", len(out), out)
	}
	if out["next"] != "v1.3.0" {
		t.Errorf("next = %q, want v1.3.0", out["next"])
	}
	if out["kind"] != "minor" {
		t.Errorf("kind = %q, want minor", out["kind"])
	}
	if _, leaked := out["NEXT"]; leaked {
		t.Errorf("NEXT (env name) leaked into result map; should be alias-keyed only")
	}
}

