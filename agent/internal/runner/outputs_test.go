func TestParseOutputsFile_AcceptsUnquotedValues(t *testing.T) {
	// Some plugins write KEY=value without quotes — parser must
	// handle both forms.
	work := t.TempDir()
	path, _ := prepareOutputsFile(work, "job-G")
	_ = os.WriteFile(path, []byte("NEXT=v1.0.0\nMSG=\"with spaces\"\n"), 0o600)
	out, err := parseOutputsFile(path, map[string]string{
		"next": "NEXT",
		"msg":  "MSG",
	})
	if err != nil {
		t.Fatal(err)
	}
	if out["next"] != "v1.0.0" {
		t.Errorf("next = %q, want v1.0.0", out["next"])
	}
	if out["msg"] != "with spaces" {
		t.Errorf("msg = %q, want with spaces", out["msg"])
	}
}

